    private void btnCrearAlumnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearAlumnoActionPerformed
        lblError.setText("");
        if (txtNombre.getText().equals("") || txtApellidos.getText().equals("")
                || txtEmail.getText().equals("") || txtEdad.getText().equals("") || txtTelefono.getText().equals("")) {
            lblError.setText("Completa todos los campos");
        } else {
            if (!compNumero(txtEdad.getText())) {
                lblError.setText("La edad tiene que ser un número");
            } else {
                String nombre = txtNombre.getText();
                String apellidos = txtApellidos.getText();
                String email = txtEmail.getText();
                int edad = Integer.parseInt(txtEdad.getText());
                String telefono = txtTelefono.getText();
                if (Principal.cont >= 5) {
                    lblError.setText("Número máximo de alumnos alcanzados");
                } else {
                    Alumno alumno = new Alumno(nombre, apellidos, email, edad, telefono);
                    Principal.alumnos[Principal.cont] = alumno;
                    Principal.cont++;
                    lblNombreJoven.setText(Alumno.alumnoMasJoven().getNombre());
                    lblApellidoJoven.setText(Alumno.alumnoMasJoven().getApellidos());
                    txtNombre.setText("");
                    txtApellidos.setText("");
                    txtEmail.setText("");
                    txtEdad.setText("");
                    txtTelefono.setText("");
                }

            }
        }

    }//GEN-LAST:event_btnCrearAlumnoActionPerformed

