    public static boolean compNumero(String n) {

        // Define la expresión regular para verificar números enteros
        String regex = "\\d+";

        // Crea un objeto Pattern para la expresión regular
        Pattern pattern = Pattern.compile(regex);

        // Crea un objeto Matcher para comparar el valor de la cadena con la expresión regular
        Matcher matcher = pattern.matcher(n);

        // Comprueba si la cadena contiene solo números
        if (matcher.matches()) {
            return true;
        } else {
            return false;
        }
    }
    

